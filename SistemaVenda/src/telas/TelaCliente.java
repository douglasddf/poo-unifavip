    private void jBAdicionaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionaClienteActionPerformed
        if (jTNomeCliente.getText().length() > 0 && jTCpfCliente.getText().length() > 0 && jTEmailCliente.getText().length() > 0 && jTTelefone.getText().length() > 0){
        String nome = jTNomeCliente.getText();
        String cpf = jTCpfCliente.getText();
        String email = jTEmailCliente.getText();
        String telefone = jTTelefone.getText();
        
        
        Cliente c = new Cliente(nome, cpf, telefone, email);
        
        repositorioCliente.addCliente(c);
        
        JOptionPane.showMessageDialog(null, c);
        
        jTNomeCliente.setText("");
        jTCpfCliente.setText("");
        jTEmailCliente.setText("");
        jTTelefone.setText("");
        
        }else{
         JOptionPane.showMessageDialog(null, "Campo(s) Em Branco");
        }
        
    }//GEN-LAST:event_jBAdicionaClienteActionPerformed

    private void jBPesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarClienteActionPerformed
        // Pesquisa
        if (jTPesquisaCliente.getText().length() != 0) {
           Cliente c = repositorioCliente.pesquisarCliente(Integer.parseInt(jTPesquisaCliente.getText()));
           
           if (c != null) {
              
               JOptionPane.showMessageDialog(null, "Cliente Encontrado");
              JOptionPane.showMessageDialog(null, c);
              
              jTPesquisaCliente.setText("");
              
              
              
           }else{
             JOptionPane.showMessageDialog(this, "NÃ£o Possui Cliente Cadastrado Com esse CÃ³digo");
             jTPesquisaCliente.setText("");

           }
        }else{
            
          JOptionPane.showMessageDialog(null, "Campo Em Branco");
        }
    }//GEN-LAST:event_jBPesquisarClienteActionPerformed

    private void jBRemoveClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoveClienteActionPerformed
        // Remove O Produto
        
        if (jTableCliente.getRowCount() != 0){
        repositorioCliente.removeCliente(jTableCliente.getSelectedRow());
        
        JOptionPane.showMessageDialog(null, "Cliente Removido Com Sucesso");
        }else {
           JOptionPane.showMessageDialog(null, "NÃ£o Possui Clientes Cadastrados No Sistema");
        }
        
        
    }//GEN-LAST:event_jBRemoveClienteActionPerformed

    private void jBEditaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditaClienteActionPerformed
        // Edita O Produto
        if(jTableCliente.getRowCount() != 0){
            
        Cliente clienteSelecionado = repositorioCliente.getCliente(jTableCliente.getSelectedRow());
        
        if (clienteSelecionado != null) {
         
          String nome = JOptionPane.showInputDialog("Nome Novo: ");
          String cpf = JOptionPane.showInputDialog("Cpf Novo: ");
          String tel = JOptionPane.showInputDialog("Telefone Novo: ");
          String email = JOptionPane.showInputDialog("Email Novo:");
          
          clienteSelecionado.setNome(nome);
          clienteSelecionado.setCpf(cpf);
          clienteSelecionado.setTelefone(tel);
          clienteSelecionado.setEmail(email);
          
          
          repositorioCliente.AtualizaTableC();
          
          
        }
        }else{
          JOptionPane.showMessageDialog(null, "NÃ£o Possui Clintes Cadastrados No Sistema");
        }
        
    }//GEN-LAST:event_jBEditaClienteActionPerformed

