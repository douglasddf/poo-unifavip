    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // salvar
        
       String nomeF = campoNome.getText();
       int cpf = campoCPF.getText();
       int idade = campoID.getText();
       String profissao = campoPRO.getText();
       String email = campoEMA.getText();
       String telefone = campoTEL.getText();

        
     
        
        if (this.funcionario != null) {
            
            // atualizar o produto existente
            
            this.funcionario.setNome(nomeF);
            this.funcionario.setCPF(cpf);
            this.funcionario.setIdade(idade);
            this.funcionario.setProfissao(profissao);
            this.funcionario.setEmail(email);
            this.funcionario.setTelefone(telefone);
                    
            RepositorioFuncionario.editarFuncionario(this.funcionario);
            JOptionPane.showMessageDialog(this, "Atualizado com Sucesso!" );
            
        } else {
            
            // criando um objeto Produto
            Funcionario funcionarioAtual = new Funcionario(nomeF, cpf, idade, profissao, email, telefone);


            // metodo opcional para visualizar dados        
            funcionarioAtual.imprimirDados(); 

            JOptionPane.showMessageDialog(this, funcionarioAtual);

            // salvar o objeto na lista de REPOSITORIO DE DADOS
            RepositorioFuncionario.adicionarFuncionario(funcionarioAtual);
        }
        
        
        
        // fechar a tela a salvar
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

